const CSS_BANNER = `/* Generated by @sugarcube-sh/cli v${packageJson.version} */\n\n`;

interface GenerateFlags {
    force?: boolean;
    silent?: boolean;
    watch?: boolean;
    resolver?: string;
    variables?: string;
    utilities?: string;
    fluidMin?: string;
    fluidMax?: string;
    colorFallback?: ColorFallbackStrategy;
    prefix?: string;
    input?: string[];
    selector?: string;
    variablesOnly?: boolean;
    utilitiesOnly?: boolean;
}

/**
 * Parse --input flags into an input object.
 * Accepts formats like: --input theme=dark --input brand=ocean
 * Returns: { theme: "dark", brand: "ocean" }
 */
function parseInputFlags(inputFlags: string[] | undefined): Record<string, string> {
    if (!inputFlags || inputFlags.length === 0) return {};

    const result: Record<string, string> = {};
    for (const flag of inputFlags) {
        const [modifier, contextValue] = flag.split("=");
        if (modifier && contextValue) {
            result[modifier] = contextValue;
        }
    }
    return result;
}

function parseFluidValue(value: string | undefined, fallback: number): number {
    return value ? Number.parseInt(value, 10) : fallback;
}

function buildFluidConfig(flags: GenerateFlags): FluidConfig | undefined {
    if (!flags.fluidMin && !flags.fluidMax) return undefined;
    return {
        min: parseFluidValue(flags.fluidMin, 320),
        max: parseFluidValue(flags.fluidMax, 1200),
    };
}

/**
 * If --input flags are provided, convert them into a single permutation
 * and override any existing config permutations.
 */
function applyInputFlags(config: InternalConfig, flags: GenerateFlags): InternalConfig {
    const input = parseInputFlags(flags.input);
    if (Object.keys(input).length === 0) return config;

    if (config.variables.permutations && config.variables.permutations.length > 0) {
        log.space(1);
        warningBoxWithBadge("Config permutations ignored due to --input flag");
    }

    return {
        ...config,
        variables: {
            ...config.variables,
            permutations: [
                {
                    input,
                    selector: flags.selector ?? ":root",
                },
            ],
        },
    };
}

function buildConfigFromFlags(flags: GenerateFlags): InternalConfig {
    const userConfig: SugarcubeConfig = {
        resolver: flags.resolver,
        variables: {
            path: flags.variables,
            prefix: flags.prefix,
            transforms: {
                fluid: buildFluidConfig(flags),
                colorFallbackStrategy: flags.colorFallback,
            },
        },
        utilities: {
            path: flags.utilities,
        },
    };

    return applyInputFlags(fillDefaults(userConfig), flags);
}

function mergeConfigWithFlags(config: InternalConfig, flags: GenerateFlags): InternalConfig {
    const merged: InternalConfig = {
        ...config,
        resolver: flags.resolver ?? config.resolver,
        variables: {
            ...config.variables,
            path: flags.variables ?? config.variables.path,
            prefix: flags.prefix ?? config.variables.prefix,
            transforms: {
                fluid: {
                    min: parseFluidValue(flags.fluidMin, config.variables.transforms.fluid.min),
                    max: parseFluidValue(flags.fluidMax, config.variables.transforms.fluid.max),
                },
                colorFallbackStrategy:
                    flags.colorFallback ?? config.variables.transforms.colorFallbackStrategy,
            },
        },
        utilities: {
            ...config.utilities,
            path: flags.utilities ?? config.utilities.path,
        },
    };

    return applyInputFlags(merged, flags);
}

function hasConfigFlags(flags: GenerateFlags): boolean {
    return !!(
        flags.resolver ||
        flags.variables ||
        flags.utilities ||
        flags.fluidMin ||
        flags.fluidMax ||
        flags.colorFallback ||
        flags.prefix
    );
}

async function resolveConfig(options: GenerateFlags): Promise<InternalConfig> {
    if (configFileExists()) {
        const { config: loadedConfig } = await loadInternalConfig();
        return mergeConfigWithFlags(loadedConfig, options);
    }

    // If --resolver is explicitly provided, use it directly
    if (options.resolver) {
        return buildConfigFromFlags(options);
    }

    // Note: We do our own discovery here instead of using loadInternalConfig's fallback
    // because generate needs special flag-merging logic that loadInternalConfig doesn't handle.
    const discovery = await findResolverDocument(process.cwd());

    if (discovery.found === "multiple") {
        throw new CLIError(ERROR_MESSAGES.GENERATE_MULTIPLE_RESOLVERS_NO_CONFIG(discovery.paths));
    }

    const resolverPath = discovery.found === "one" ? discovery.path : undefined;

    if (resolverPath || hasConfigFlags(options)) {
        return buildConfigFromFlags({ ...options, resolver: resolverPath });
    }

    throw new CLIError(ERROR_MESSAGES.GENERATE_NO_CONFIG_OR_RESOLVER());
}

function formatOutputPaths(output: CSSFileOutput): string[] {
    const paths = output.map((f) => f.path);
    const unique = [...new Set(paths)];
    return unique.map((file) => relative(process.cwd(), file));
}

async function generateSugarcubeUtilities(
    tokens: NormalizedConvertedTokens,
    config: InternalConfig
): Promise<CSSFileOutput> {
    if (!config.utilities.classes || Object.keys(config.utilities.classes).length === 0) {
        return [];
    }

    const sugarcubePreset = {
        name: "sugarcube",
        rules: convertConfigToUnoRules(config.utilities.classes, tokens),
        preflights: [],
    };

    const generatorOptions: UserConfig = {
        presets: [sugarcubePreset],
    };

    const generator = await createGenerator(generatorOptions);

    const files = await getMarkupFiles();
    if (files.length === 0) return [];

    const sources = await readMarkupSources(files);
    const combinedSource = sources.join("\n");

    const { css } = await generator.generate(combinedSource, { preflights: false });
    if (!css?.trim()) return [];

    return [
        {
            path: config.utilities.path,
            css,
        },
    ];
}

function addBanner(output: CSSFileOutput): CSSFileOutput {
    return output.map((file) => ({
        ...file,
        css: CSS_BANNER + file.css,
    }));
}

function wrapInLayer(output: CSSFileOutput, layerName: string): CSSFileOutput {
    return output.map((file) => {
        // Need extra formatting for @layer blocks
        const indentedCss = file.css
            .split("\n")
            .map((line) => (line.trim() ? `    ${line}` : line))
            .join("\n");
        return {
            ...file,
            css: `@layer ${layerName} {\n${indentedCss}}\n`,
        };
    });
}

interface GenerateAllCSSOptions {
    variablesOnly?: boolean;
    utilitiesOnly?: boolean;
}

async function generateAllCSS(
    trees: TokenTree[],
    resolved: ResolvedTokens,
    config: InternalConfig,
    options: GenerateAllCSSOptions = {}
): Promise<CSSFileOutput> {
    const output: CSSFileOutput = [];

    const convertedTokens = await convertTokens(trees, resolved, config);

    if (!options.utilitiesOnly) {
        let cssVariables = await generateCSSVariables(convertedTokens, config);
        if (config.variables.layer) {
            cssVariables = wrapInLayer(cssVariables, config.variables.layer);
        }
        const cssVariablesWithBanner = addBanner(cssVariables);
        await writeCSSVariablesToDisk(cssVariablesWithBanner);
        output.push(...cssVariablesWithBanner);
    }

    if (!options.variablesOnly) {
        let utilities = await generateSugarcubeUtilities(convertedTokens, config);
        if (config.utilities.layer) {
            utilities = wrapInLayer(utilities, config.utilities.layer);
        }
        const utilitiesWithBanner = addBanner(utilities);
        await writeCSSUtilitiesToDisk(utilitiesWithBanner);
        output.push(...utilitiesWithBanner);
    }

    return output;
}

type GenerationResult = {
    output: CSSFileOutput;
    warnings: Array<{ path: string; message: string }>;
};

async function runGeneration(
    config: InternalConfig,
    options: GenerateAllCSSOptions = {}
): Promise<GenerationResult> {
    clearMatchCache();
    const { trees, resolved, warnings } = await prepareTokens(config);
    const output = await generateAllCSS(trees, resolved, config, options);
    return { output, warnings };
}

function displayWarnings(warnings: Array<{ path: string; message: string }>): void {
    if (warnings.length === 0) return;

    const warningMessages = warnings.map((w) => w.message).join("\n\n");
    log.space(1);
    warningBoxWithBadge(warningMessages);
}

async function logOneTimeResult(relativePaths: string[]): Promise<void> {
    const tasks = relativePaths.map((file) => ({
        pending: `Write ${file}`,
        start: `Writing ${file}`,
        end: `Wrote ${file}`,
        while: async () => {},
    }));
    await log.tasks(tasks, {
        spacing: 1,
        minDurationMs: 0,
        successPauseMs: 100,
        successMessage: "🎉 Files written!",
    });
    log.space(1);
    outro(color.green("CSS generated successfully."));
}

async function runOneTimeGeneration(config: InternalConfig, options: GenerateFlags): Promise<void> {
    const { output, warnings } = await runGeneration(config, {
        variablesOnly: options.variablesOnly,
        utilitiesOnly: options.utilitiesOnly,
    });

    if (!options.silent) {
        displayWarnings(warnings);
        await logOneTimeResult(formatOutputPaths(output));
    }
}

async function runWatchMode(config: InternalConfig, options: GenerateFlags): Promise<void> {
    const prefix = color.cyan("[sugarcube]");
    const warnPrefix = color.yellow("[sugarcube]");
    const generateOptions: GenerateAllCSSOptions = {
        variablesOnly: options.variablesOnly,
        utilitiesOnly: options.utilitiesOnly,
    };

    const logWarnings = (warnings: Array<{ path: string; message: string }>) => {
        for (const warning of warnings) {
            console.log(`${warnPrefix} ${warning.message}`);
        }
    };

    const startTime = performance.now();
    const { output: initialOutput, warnings: initialWarnings } = await runGeneration(
        config,
        generateOptions
    );
    const durationMs = Math.round(performance.now() - startTime);
    logWarnings(initialWarnings);
    console.log(`${prefix} Generated in ${durationMs}ms`);

    const watcher = await startWatcher(config, {
        onRegenerate: async (changedPath: string) => {
            const regenStart = performance.now();
            const { output: regenOutput, warnings: regenWarnings } = await runGeneration(
                config,
                generateOptions
            );
            const regenDurationMs = Math.round(performance.now() - regenStart);

            logWarnings(regenWarnings);
            const changedFile = changedPath.split("/").pop() ?? changedPath;
            const outputFiles = formatOutputPaths(regenOutput);
            console.log(
                `${prefix} ${color.dim(changedFile)} → ${outputFiles.join(", ")} ${color.dim(`(${regenDurationMs}ms)`)}`
            );
        },
        onError: (error: Error) => {
            console.log(`${color.red("[sugarcube]")} ${error.message}`);
        },
        onReady: (tokenFileCount: number) => {
            console.log(
                `${prefix} Watching ${tokenFileCount} token file${tokenFileCount === 1 ? "" : "s"} + markup files...`
            );
        },
    });

    process.on("SIGINT", async () => {
        await watcher.close();
        console.log(`${prefix} Watch mode stopped.`);
        process.exit(0);
    });

    await new Promise(() => {});
}

export const generate = new Command()
    .name("generate")
    .description("Generate CSS from your design tokens")
    .option("--force", "Skip overwrite confirmation")
    .option("-s, --silent", "Suppress logs and prompts")
    .option("-w, --watch", "Watch for changes and regenerate automatically")
    .option("--resolver <path>", "Path to token resolver file (.resolver.json)")
    .option(
        "--variables <path>",
        "Output path for CSS variables (default: 'src/styles/tokens.css')"
    )
    .option(
        "--utilities <path>",
        "Output path for utility classes (default: 'src/styles/utilities.css')"
    )
    .option("--fluid-min <number>", "Minimum viewport width for fluid scaling (default: 320)")
    .option("--fluid-max <number>", "Maximum viewport width for fluid scaling (default: 1200)")
    .option(
        "--color-fallback <strategy>",
        "Color fallback strategy: 'native' or 'polyfill' (default: native)"
    )
    .option("--prefix <string>", "Prefix prepended to every generated CSS variable name")
    .option(
        "--input <modifier=value>",
        "Select a modifier context for this build (repeatable)",
        (value: string, previous: string[]) => previous.concat([value]),
        [] as string[]
    )
    .option("--selector <selector>", "CSS selector for --input output (default: ':root')")
    .option("--variables-only", "Generate only CSS variables, skip utilities")
    .option("--utilities-only", "Generate only utilities, skip CSS variables")
    .action(async (options: GenerateFlags) => {
        try {
            if (!options.silent && !options.watch) {
                intro(label("Generate CSS"));
            }

            const finalConfig = await resolveConfig(options);

            const run = options.watch ? runWatchMode : runOneTimeGeneration;
            await run(finalConfig, options);
        } catch (error) {
            handleError(error);
        }
    });
